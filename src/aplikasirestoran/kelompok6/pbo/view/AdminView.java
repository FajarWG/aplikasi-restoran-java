    public void tampilTransaksi(){
        String [] judul = {"ID_TRANSAKSI", "NAMA PELANGGAN","NAMA MAKANAN","HARGA MAKANAN","TOTAL MAKANAN", "NAMA MINUMAN","HARGA MINUMAN","TOTAL MINUMAN", "TOTAL BAYAR"};
        DefaultTableModel model;
        
        model = new DefaultTableModel(judul, 0);
        tableTransaksi.setModel(model);
        
        try {
            Connection con = new Koneksi().konek();
            String sql = "SELECT transaksi.*, makanan.nama_makanan, minuman.nama_minuman, makanan.harga_makanan, minuman.harga_minuman FROM transaksi INNER JOIN makanan ON transaksi.id_makanan = makanan.id_makanan INNER JOIN minuman ON transaksi.id_minuman = minuman.id_minuman ;";
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            
            while (rs.next()){
                String id= rs.getString ("id_transaksi");
                String nama= rs.getString("nama_pelanggan");
                String makanan = rs.getString("nama_makanan");
                String hmakanan=rs.getString("harga_makanan");
                String tmakanan= rs.getString ("total_makanan");
                String minuman= rs.getString("nama_minuman");
                String hminuman=rs.getString("harga_minuman");
                String tminuman= rs.getString("total_minuman");
                String total= rs.getString("total_bayar");
                
                String [] data = {id,nama,makanan,hmakanan,tmakanan,minuman,hminuman,tminuman,total};
                model.addRow(data);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            
        }
    }
    public void tampilPenghasilan(){
        int total=0;
        for(int i =0; i<tableTransaksi.getRowCount();i++){
            int hitung = Integer.parseInt((String)tableTransaksi.getValueAt(i,8));
            total+=hitung;
        }
        lblPenghasilan.setText(""+total);
        int angka;
        
        angka= Integer.parseInt(lblPenghasilan.getText());
        String ganti = NumberFormat.getNumberInstance(Locale.US).format(angka);
        StringTokenizer token = new StringTokenizer(ganti,".");
        ganti=ganti.replace(",",".");
        lblPenghasilan.setText("RP. "+ String.format(ganti));
    }

    public void tampilPenjualan(){
        try {
            Connection con = new Koneksi().konek();
            String sql = "SELECT COUNT(*) AS transaksicount FROM transaksi ";
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            
            while (rs.next()){
               int count = rs.getInt("transaksicount");
               lblPenjualan.setText(String.valueOf(count));
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            
        }
    }

    public void tampilMakanan(){
        
        String [] judul = {"ID MAKANAN", "NAMA MAKANAN", "HARGA"};
        DefaultTableModel model;
        
        model = new DefaultTableModel(judul, 0);
        tableMakanan.setModel(model);
        
        try {
            Connection con = new Koneksi().konek();
            String sql = "SELECT * FROM makanan";
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            
            while (rs.next()){
                String id = rs.getString ("id_makanan");
                String nama = rs.getString("nama_makanan");
                String harga = rs.getString("harga_makanan");
                
                String [] data = {id, nama, harga };
                model.addRow(data);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            
        }
    }
